// ─────────────────────────────────────────────────────────────
// Історія розмови в пам'яті (per chatId)
// У продакшені на Vercel serverless вона теплюча в межах одного інстансу
// (10-15 хв активності). Після холодного старту скидається — для MVP ок.
// ─────────────────────────────────────────────────────────────
type ChatMessage = { role: "user" | "assistant"; content: string };
const historyStore = new Map<
  number,
  { messages: ChatMessage[]; lastActive: number }
>();

const HISTORY_TTL_MS = 30 * 60 * 1000; // 30 хвилин
const MAX_HISTORY = 20;

export function getHistory(chatId: number): ChatMessage[] {
  const entry = historyStore.get(chatId);
  if (!entry) return [];
  if (Date.now() - entry.lastActive > HISTORY_TTL_MS) {
    historyStore.delete(chatId);
    return [];
  }
  return entry.messages;
}

export function pushHistory(chatId: number, msg: ChatMessage): void {
  const existing = getHistory(chatId);
  const updated = [...existing, msg].slice(-MAX_HISTORY);
  historyStore.set(chatId, { messages: updated, lastActive: Date.now() });
}

export function clearHistory(chatId: number): void {
  historyStore.delete(chatId);
}

// Періодичне прибирання застарілих сесій (при кожному запиті)
export function pruneOldSessions(): void {
  const now = Date.now();
  for (const [id, entry] of historyStore.entries()) {
    if (now - entry.lastActive > HISTORY_TTL_MS) {
      historyStore.delete(id);
    }
  }
}
